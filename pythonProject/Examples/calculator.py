def add(num1, num2):
    """

    :param num1:
    :param num2:
    :return: sum of num1 and num2
    """
    return num1 + num2


def sub(num1, num2):
    """

    :param num1:
    :param num2:
    :return: subtraction of num1 and num2
    """
    return num1 - num2


def mul(num1, num2):
    """

    :param num1:
    :param num2:
    :return: Multiplication of num1 and num2
    """
    return num1 * num2


